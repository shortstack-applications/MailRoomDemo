    //Removes staff member from list
    private void removeStaffMemberActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeStaffMemberActionPerformed
        // Removes staff member from stafflist
        String aStaffMember = (String)listCurrentStaff.getSelectedValue();
        String response = mailRoom.removeStaffMember(aStaffMember);
        staffResults.setText(response);
        listCurrentStaff.setListData(new Vector<String>(mailRoom.getStaffDetails(mailRoom.getStaffMembers())));
    }//GEN-LAST:event_removeStaffMemberActionPerformed

    
    //adds new staff member to list
    private void addStaffMemberActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addStaffMemberActionPerformed
        // Add staff member details to new StaffMember object
        String response = mailRoom.addStaffMember(firstName.getText(), lastName.getText(), staffCount);
        staffResults.setText(response);
        firstName.setText("");
        lastName.setText("");
        staffCount++;
        listCurrentStaff.setListData(new Vector<String>(mailRoom.getStaffDetails(mailRoom.getStaffMembers())));
        staffList.setListData(new Vector<String>(mailRoom.getStaffDetails(mailRoom.getStaffMembers())));
    }//GEN-LAST:event_addStaffMemberActionPerformed

    
    
    //dispatches a selected parcel from list 
    private void dispatchParcelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_dispatchParcelActionPerformed
        // Dispatches selected Parcel from list
        LocalDateTime curTime = LocalDateTime.now();
        DateTimeFormatter formatTime = DateTimeFormatter.ofPattern("dd-MM-yyyy HH:mm:ss");
        String formattedTime = curTime.format(formatTime);
        
        String selectedParcel = (String)listOfParcels.getSelectedValue();
        String response = mailRoom.dispatchParcel(selectedParcel, formattedTime);
        listOfParcels.setListData(new Vector<String>(mailRoom.getParcelList(mailRoom.getParcels())));
        recordResults2.setText(response);
    }//GEN-LAST:event_dispatchParcelActionPerformed

